public class warmup1
{
    public static void main(String args[])
    {
	System.out.println("1 to 100");
	for(int a = 1; a <= 100; a++)
	    {
		System.out.println("" + a);
	    }
	System.out.println("\n\n100 to 1");
	for(int b = 100; b >= 1; b--)
	    {
		System.out.println("" + a);
	    }
    }
}
